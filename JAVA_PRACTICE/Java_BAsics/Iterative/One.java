package Iterative;
// here we are printing the 1 to 100 using for loop
public class One {
    public static void main(String[] args) {
        for(int i=1;i<=100;i++){
            System.out.println(i);
        }
    }
}

/*for executing 
 * if we declare the package--->  package Iterative;
 * compiling-->    javac Iterative/one.java
 * executing--->   java Iterative.One
 */